package BitManipulation;

public class BasicManipulation {
    public static void main(String[] args) {
        System.out.println(5 & 4);
        System.out.println(5 | 4);
        System.out.println(~(0));
        System.out.println(5 << 2); // a << b = a * 2 ^ b
        System.out.println(6 >> 1); // a >> b = a / 2^b
    }
}
